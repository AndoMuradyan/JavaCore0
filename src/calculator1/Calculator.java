package calculator1;

public class Calculator {

    double plus(double a, double s) {
        double result = a + s;
        return result;
    }

    double minus(double d, double f) {
        double result = d - f;
        return result;
    }

    double multiply(double g, double h) {
        double result = g * h;
        return result;
    }

    double divide(double t, double q) {
        double result = t / q;
        return result;
    }


}
